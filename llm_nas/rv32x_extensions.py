import numpy as np
from typing import Dict, List, Any

class RV32XExtensions:
    """
    Implements three custom RV32X-SQ instructions:
    1. MAC4: 4-bit multiply-accumulate
    2. LIF: Leaky Integrate-and-Fire neuron update
    3. FUSION: Attention-based feature fusion
    """
    
    def __init__(self):
        self.instructions = {
            'MAC4': {'opcode': '0x0B', 'description': '4-bit multiply-accumulate', 'cycles': 1},
            'LIF': {'opcode': '0x0C', 'description': 'LIF neuron update', 'cycles': 1},
            'FUSION': {'opcode': '0x0D', 'description': 'Attention-based fusion', 'cycles': 2}
        }
        
        self.performance_gains = {
            'MAC4': '2× throughput for 4-bit operations',
            'LIF': '90% reduction in neuron update cycles',
            'FUSION': '40% latency improvement for hybrid models'
        }
        
        print(f"✅ RV32X-SQ extensions initialized")
        print(f"   Instructions: {', '.join(self.instructions.keys())}")
    
    def mac4(self, a: int, b: int, acc: int) -> int:
        """
        MAC4: 4-bit signed multiply-accumulate
        Performs: acc = acc + (a × b)
        a, b are 4-bit signed integers (-8 to 7)
        """
        # Sign-extend from 4 bits to 32 bits
        a_signed = a if a < 8 else a - 16
        b_signed = b if b < 8 else b - 16
        
        result = acc + (a_signed * b_signed)
        return result
    
    def lif(self, input_val: int, membrane: int, threshold: int, leak: int = 0) -> tuple:
        """
        LIF: Leaky Integrate-and-Fire neuron update
        Implements: v(t+1) = v(t) + input - leak
        Spike if v > threshold
        """
        # Integrate
        new_membrane = membrane + input_val - leak
        
        # Fire if threshold exceeded
        spike = 1 if new_membrane > threshold else 0
        
        # Reset if fired
        if spike:
            new_membrane = membrane  # Reset to previous value
        
        return new_membrane, spike
    
    def fusion(self, snn_out: int, qnn_out: int, attention_weight: int) -> int:
        """
        FUSION: Attention-based feature fusion
        Combines SNN (temporal) and QNN (spatial) features
        """
        # Weighted fusion with 8-bit attention weight
        # Result = snn_out * w + qnn_out * (256 - w)
        w = attention_weight
        result = (snn_out * w + qnn_out * (256 - w)) // 256
        return result
    
    def get_configuration(self) -> Dict:
        """Return RV32X-SQ configuration"""
        return {
            "extensions_enabled": True,
            "instructions": self.instructions,
            "performance_gains": self.performance_gains,
            "custom_opcodes": {
                "MAC4": "0x0B (0001011)",
                "LIF": "0x0C (0001100)",
                "FUSION": "0x0D (0001101)"
            },
            "encoding_format": {
                "funct3": "3 bits",
                "funct7": "7 bits",
                "rs1": "source register 1",
                "rs2": "source register 2",
                "rd": "destination register"
            },
            "integration_notes": "Add to RISC-V core's decoder and ALU"
        }
    
    def generate_verilog_rtl(self) -> str:
        """
        Generate Verilog RTL for RV32X-SQ custom instruction unit
        As described in paper Listing 1
        """
        return """/**
 * RV32X-SQ Custom Instruction Unit
 * Generated by LLM-NAS AutoCoDesign Framework
 * 
 * Implements three custom instructions:
 * - MAC4: 4-bit multiply-accumulate
 * - LIF: Leaky Integrate-and-Fire neuron update
 * - FUSION: Attention-based feature fusion
 */

module rv32x_sq_unit (
    input  logic        clk,
    input  logic        rst_n,
    input  logic [31:0] inst,
    input  logic [31:0] rs1_data,
    input  logic [31:0] rs2_data,
    output logic [31:0] rd_data,
    input  logic        valid,
    output logic        ready
);

    // Custom instruction opcodes
    localparam OP_MAC4   = 7'b0001011;
    localparam OP_LIF    = 7'b0001100;
    localparam OP_FUSION = 7'b0001101;
    
    // Instruction decode
    logic is_mac4, is_lif, is_fusion;
    assign is_mac4   = (inst[31:25] == OP_MAC4);
    assign is_lif    = (inst[31:25] == OP_LIF);
    assign is_fusion = (inst[31:25] == OP_FUSION);
    
    // ========== MAC4 Unit ==========
    logic [31:0] mac4_acc;
    logic [3:0]  mac4_a, mac4_b;
    
    assign mac4_a = rs1_data[3:0];
    assign mac4_b = rs2_data[3:0];
    
    always_ff @(posedge clk or negedge rst_n) begin
        if (!rst_n)
            mac4_acc <= 32'b0;
        else if (valid && is_mac4) begin
            // Sign-extend 4-bit to 32-bit
            automatic logic [31:0] a_ext = {{28{mac4_a[3]}}, mac4_a};
            automatic logic [31:0] b_ext = {{28{mac4_b[3]}}, mac4_b};
            mac4_acc <= mac4_acc + (a_ext * b_ext);
        end
    end
    
    // ========== LIF Unit ==========
    logic [7:0] lif_mem, lif_input, lif_thresh, lif_leak;
    logic       lif_spike;
    
    assign lif_input  = rs1_data[7:0];
    assign lif_mem    = rs2_data[15:8];
    assign lif_thresh = rs2_data[23:16];
    assign lif_leak   = lif_mem >>> 3;  // Divide by 8
    
    always_comb begin
        logic [8:0] temp;
        temp = $signed(lif_mem) + $signed(lif_input) - $signed(lif_leak);
        lif_spike = (temp[7:0] > lif_thresh);
    end
    
    logic [7:0] lif_mem_state;
    always_ff @(posedge clk or negedge rst_n) begin
        if (!rst_n)
            lif_mem_state <= 8'b0;
        else if (valid && is_lif)
            lif_mem_state <= lif_spike ? lif_mem : lif_mem + lif_input - lif_leak;
    end
    
    // ========== FUSION Unit ==========
    logic [7:0] fusion_snn, fusion_qnn, fusion_weight;
    logic [15:0] fusion_result;
    
    assign fusion_snn    = rs1_data[7:0];
    assign fusion_qnn    = rs1_data[15:8];
    assign fusion_weight = rs2_data[7:0];
    
    // Weighted fusion: result = snn * w + qnn * (256 - w)
    assign fusion_result = fusion_snn * fusion_weight + 
                          fusion_qnn * (256 - fusion_weight);
    
    // ========== Output Selection ==========
    always_comb begin
        case (inst[31:25])
            OP_MAC4:   rd_data = mac4_acc;
            OP_LIF:    rd_data = {24'b0, lif_mem_state, lif_spike};
            OP_FUSION: rd_data = {16'b0, fusion_result[15:0]};
            default:   rd_data = 32'b0;
        endcase
    end
    
    assign ready = 1'b1;

endmodule
"""
    
