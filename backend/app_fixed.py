@app.route('/api/projects', methods=['GET'])
def list_projects():
    projects = []
    proj_dir = "../generated_projects"
    if os.path.exists(proj_dir):
        projects = os.listdir(proj_dir)
    
    return jsonify({
        'count': len(projects),
        'projects': projects[-5:]  # 5 derniers
    })

@app.route('/api/download/<project_id>', methods=['GET'])
def download_project(project_id):
    project_dir = f"../generated_projects/{project_id}"
    if not os.path.exists(project_dir):
        return jsonify({'error': 'Project not found'}), 404
    
    # Créer ZIP
    memory_file = io.BytesIO()
    with zipfile.ZipFile(memory_file, 'w') as zipf:
        for root, dirs, files in os.walk(project_dir):
            for file in files:
                file_path = os.path.join(root, file)
                arcname = os.path.relpath(file_path, project_dir)
                zipf.write(file_path, arcname)
    
    memory_file.seek(0)
    return send_file(memory_file, mimetype='application/zip', as_attachment=True, download_name=f'{project_id}.zip')

