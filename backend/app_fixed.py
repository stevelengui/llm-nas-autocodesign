from flask import Flask, jsonify, request, send_file
from flask_cors import CORS
import os
import json
import zipfile
import io
from datetime import datetime
import sys

app = Flask(__name__)
CORS(app)

# ========== ROUTES ESSENTIELLES POUR LE FRONTEND ==========

@app.route('/api/health')
def health():
    return jsonify({
        "status": "healthy",
        "time": datetime.now().isoformat(),
        "version": "2.0.0"
    })

